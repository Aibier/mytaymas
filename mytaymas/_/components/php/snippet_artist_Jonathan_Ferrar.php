	<h2>Jonathan G. Ferrar II</h2>
	<img class="pull-left img-rounded" src="images/artists/Jonathan_Ferrar_tn.jpg" alt="Photo of Jonathan Ferrar">
	<div class="media-body">
			<p>Labeled as "The Artist to Watch in 2012" by the London Review, Johnathan has already sold one of the highest priced-commissions paid to an art student, ever on record. The piece, entitled Gratitude Resort, a work in oil and mixed media, was sold for $750,000 and Jonathan donated all the proceeds to Art for Peace, an organization that provides college art scholarships for creative children in developing nations.</p>
			<p>Jonathan's interest in the non-profit sector has helped raise money for a variety of other organizations through art-based fundraisers and art sales. He has become known as the "Phil-art-thropist" in non-profit circles – a title that, when asked about, garners a sheepish grin from Jonathan. Jonathan studied non-profit management at London Business School, and is happy to have found his niche in his two favorite worlds – art and philanthropy.</p>
